#!/usr/bin/env node
/**
 * Build src/data/locations.ts from a CSV of UK locations.
 *
 * CSV columns (no header):
 *   1. Town name
 *   2. Latitude
 *   3. Longitude
 *   4. Postcode prefix
 *   5. Region
 *   6. County
 *
 * Rows where town name == county name are treated as the county centroid
 * (used to seed county metadata) and are not emitted as town pages.
 *
 * Usage:
 *   node scripts/build-locations.mjs /path/to/locations.csv
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const PROJECT_ROOT = path.resolve(__dirname, '..');

const csvPath = process.argv[2];
if (!csvPath) {
  console.error('Usage: node scripts/build-locations.mjs <path-to-csv>');
  process.exit(1);
}
if (!fs.existsSync(csvPath)) {
  console.error(`CSV not found: ${csvPath}`);
  process.exit(1);
}

const slugify = (s) =>
  s
    .toLowerCase()
    .normalize('NFKD')
    .replace(/['’`]/g, '')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
    .replace(/-+/g, '-');

const rows = fs
  .readFileSync(csvPath, 'utf8')
  .split('\n')
  .map((l) => l.trim())
  .filter(Boolean)
  .map((line) => {
    const cols = line.split(',').map((c) => c.trim());
    return {
      name: cols[0],
      lat: cols[1] ? Number(cols[1]) : undefined,
      lng: cols[2] ? Number(cols[2]) : undefined,
      postcode: cols[3] || undefined,
      region: cols[4],
      county: cols[5],
    };
  });

const countyMap = new Map();

for (const row of rows) {
  if (!countyMap.has(row.county)) {
    countyMap.set(row.county, {
      slug: slugify(row.county),
      name: row.county,
      region: row.region,
      lat: undefined,
      lng: undefined,
      towns: new Map(),
    });
  }
  const county = countyMap.get(row.county);

  // County-level row: town name == county name, blank postcode
  if (row.name === row.county) {
    county.lat = row.lat;
    county.lng = row.lng;
    continue;
  }

  const townSlug = slugify(row.name);
  if (county.towns.has(townSlug)) continue;
  county.towns.set(townSlug, {
    slug: townSlug,
    name: row.name,
    lat: row.lat,
    lng: row.lng,
    postcode: row.postcode,
  });
}

const counties = Array.from(countyMap.values())
  .map((c) => ({
    slug: c.slug,
    name: c.name,
    region: c.region,
    lat: c.lat,
    lng: c.lng,
    towns: Array.from(c.towns.values()).sort((a, b) =>
      a.name.localeCompare(b.name),
    ),
  }))
  .sort((a, b) => a.name.localeCompare(b.name));

const totalTowns = counties.reduce((sum, c) => sum + c.towns.length, 0);

const header = `// AUTO-GENERATED by scripts/build-locations.mjs
// To regenerate: node scripts/build-locations.mjs <path-to-csv>
// Edit the CSV (or this file directly) — do not edit auto-generated sections by hand
// unless you are also rerunning the script.

`;

const types = `export type Town = {
  slug: string;
  name: string;
  lat?: number;
  lng?: number;
  postcode?: string;
  intro?: string;
};

export type County = {
  slug: string;
  name: string;
  region: string;
  lat?: number;
  lng?: number;
  intro?: string;
  towns: Town[];
};

`;

const dataLiteral = `export const counties: County[] = ${JSON.stringify(counties, null, 2)};

`;

const helpers = `export const getCountyBySlug = (slug: string): County | undefined =>
  counties.find((c) => c.slug === slug);

export const getTownInCounty = (
  countySlug: string,
  townSlug: string,
): { county: County; town: Town } | undefined => {
  const county = getCountyBySlug(countySlug);
  if (!county) return undefined;
  const town = county.towns.find((t) => t.slug === townSlug);
  if (!town) return undefined;
  return { county, town };
};

export const getSiblingTowns = (
  countySlug: string,
  currentTownSlug: string,
): Town[] => {
  const county = getCountyBySlug(countySlug);
  if (!county) return [];
  return county.towns.filter((t) => t.slug !== currentTownSlug);
};

export const countiesByRegion = (): Record<string, County[]> =>
  counties.reduce<Record<string, County[]>>((acc, county) => {
    if (!acc[county.region]) acc[county.region] = [];
    acc[county.region].push(county);
    return acc;
  }, {});
`;

const out = header + types + dataLiteral + helpers;
const outPath = path.join(PROJECT_ROOT, 'src/data/locations.ts');
fs.writeFileSync(outPath, out);

console.log(`Wrote ${outPath}`);
console.log(`  ${counties.length} counties`);
console.log(`  ${totalTowns} towns`);
